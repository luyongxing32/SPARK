public class TwoRowItem {

	public final String id;
	public final boolean invited;
	public final String name;
	public final String title;

	public TwoRowItem(String id, boolean invited, String name, String title) {
		this.id = id;
		this.invited = invited;
		this.name = name;
		this.title = title;
	}
	
}
